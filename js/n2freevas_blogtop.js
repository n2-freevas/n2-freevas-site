function pushArtIcon(){
    $(".art").css("display","block");
    $(".physical").css("display","none");
    $(".study").css("display","none");
    $(".other").css("display","none");
}

function pushPhysicalIcon(){
    $(".art").css("display","none");
    $(".physical").css("display","block");
    $(".study").css("display","none");
    $(".other").css("display","none");
}

function pushStudyIcon(){
    $(".art").css("display","none");
    $(".physical").css("display","none");
    $(".study").css("display","block");
    $(".other").css("display","none");
}

function pushOtherIcon(){
    $(".art").css("display","none");
    $(".physical").css("display","none");
    $(".study").css("display","none");
    $(".other").css("display","block");
}

function pushSeeallIcon(){
    $(".art").css("display","block");
    $(".physical").css("display","block");
    $(".study").css("display","block");
    $(".other").css("display","block");
}